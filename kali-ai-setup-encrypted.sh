        echo -e " ${GREEN}✓ 解密成功${NC}"; echo ""
        cleanup() { [ -f "$TMPFILE" ] && { shred -u "$TMPFILE" 2>/dev/null || rm -f "$TMPFILE"; }; }
        trap cleanup EXIT; bash "$TMPFILE"; exit $?
    else
        rm -f "$TMPFILE"; echo -e " ${RED}✗ 密码错误${NC}"; TRIES=$((TRIES+1))
        [ $TRIES -lt $MAX_TRIES ] && echo -e "  ${DIM}剩余尝试: $((MAX_TRIES-TRIES))${NC}"
    fi
done
echo ""; echo -e "  ${RED}⛔ 密码错误次数过多，退出。${NC}"; echo ""; exit 1
