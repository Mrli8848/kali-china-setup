echo ""
echo -e "  ${BOLD}${CYAN}╔══════════════════════════════════════════════╗${NC}"
echo -e "  ${BOLD}${CYAN}║   Kali AI + 中文可视化 v3.0${NC}"
echo -e "  ${BOLD}${CYAN}║   Author: Mr.li8848                         ║${NC}"
echo -e "  ${BOLD}${CYAN}╚══════════════════════════════════════════════╝${NC}"
echo ""
[ "$(id -u)" -ne 0 ] && echo -e "  ${RED}⛔ 请使用 sudo 运行！${NC}" && exit 1
command -v openssl &>/dev/null || { echo -e "  ${RED}⛔ 需要 openssl${NC}"; exit 1; }
MAX_TRIES=3; TRIES=0
while [ $TRIES -lt $MAX_TRIES ]; do
    echo ""; echo -ne "  ${YELLOW}🔑 请输入解密密码: ${NC}"; read -s PASSWORD; echo ""
    [ -z "$PASSWORD" ] && echo -e "  ${RED}密码不能为空！${NC}" && TRIES=$((TRIES+1)) && continue
    echo -ne "  ${DIM}正在解密...${NC}"
    PAYLOAD_B64=$(sed -n '/^__PAYLOAD_START__$/,/^__PAYLOAD_END__$/p' "$0" | sed '1d;$d')
    TMPFILE=$(mktemp /tmp/kali-setup-XXXXXX.sh); chmod 700 "$TMPFILE"
    printf '%s' "$PAYLOAD_B64" | openssl enc -aes-256-cbc -pbkdf2 -iter 100000 -salt -d -base64 -pass pass:"$PASSWORD" -out "$TMPFILE" 2>/dev/null && D=1 || D=0
    if [ "$D" = "1" ] && [ -s "$TMPFILE" ] && head -1 "$TMPFILE" | grep -q '^#!/bin/bash'; then
        echo -e " ${GREEN}✓ 解密成功${NC}"; echo ""
        cleanup() { [ -f "$TMPFILE" ] && { shred -u "$TMPFILE" 2>/dev/null || rm -f "$TMPFILE"; }; }
        trap cleanup EXIT; bash "$TMPFILE"; exit $?
    else
        rm -f "$TMPFILE"; echo -e " ${RED}✗ 密码错误${NC}"; TRIES=$((TRIES+1))
        [ $TRIES -lt $MAX_TRIES ] && echo -e "  ${DIM}剩余尝试: $((MAX_TRIES-TRIES))${NC}"
    fi
done
echo ""; echo -e "  ${RED}⛔ 密码错误次数过多，退出。${NC}"; echo ""; exit 1
