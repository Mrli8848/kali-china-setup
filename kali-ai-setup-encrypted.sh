[ "$(id -u)" -ne 0 ] && echo -e "  ${RED}⛔ 请使用 sudo 运行！${NC}" && exit 1
command -v openssl &>/dev/null || { echo -e "  ${RED}⛔ 需要 openssl${NC}"; exit 1; }
MAX_TRIES=3; TRIES=0
while [ $TRIES -lt $MAX_TRIES ]; do
    echo ""; echo -ne "  ${YELLOW}🔑 请输入解密密码: ${NC}"; read -s PASSWORD; echo ""
    [ -z "$PASSWORD" ] && echo -e "  ${RED}密码不能为空！${NC}" && TRIES=$((TRIES+1)) && continue
